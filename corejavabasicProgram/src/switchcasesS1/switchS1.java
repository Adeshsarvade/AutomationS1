package switchcasesS1;

public class switchS1 {

	public static void main(String[] args) {
		// TODO switch cases 
		// you can break the value of variables if the value dosn't match as per the given value it will get default.
		
		int num=25;
		switch (num) {
		case 15:
		System.out.println("number is counter 15");
		break;
		case 25:
		System.out.println("number is counter 25");
		break;
		case 30:
			System.out.println("number is counter 30");
			break;
			default:
			System.out.println("number is non countable");

		}
		

	}

}
